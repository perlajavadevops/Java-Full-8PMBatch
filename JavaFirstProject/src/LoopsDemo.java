
public class LoopsDemo {

	public static void main(String[] args) {
		// 0 to 10;
		/*
		 * System.out.println(1); System.out.println(1); System.out.println(1);
		 * System.out.println(1); System.out.println(1); System.out.println(1);
		 */
		// for, while, do-while

		// for --> for(stem1; stem2; stem3){syso(stem1)}

		/*
		 * for (int i = 0; i <= 10; i++) { // 10 to 0 System.out.println(i); }
		 */

		//A person --> 
		int i = 0;
		/*
		 * while (i <= 10) { System.out.println(i); i++; }
		 */
		
		do {
			System.out.println(i);
			i++;
		}while(i <= 10);
		
		/*
		 * System.out.println("============="); for(int i = 10; i>=0; i--) { // 10 to 0
		 * System.out.println(i); }
		 */
	}
}


